"""Snapshot (characterization) tests pinning the three benchmark results.

These freeze the numeric outputs of the scripts in ``benchmarks/`` at their
exact parameters, so any future code change that alters the computed Berry
curvature, group velocity, or quasi-energy spectrum is caught. Values are
compared with a finite ``rtol`` (plus a small ``atol`` for genuinely near-zero
quantities), not bit-exact equality: last-bit LAPACK/BLAS differences across
platforms should not cause spurious failures, while real regressions -- which
move results far more than ``rtol`` -- still fail.

Fragile points are deliberately excluded (see the benchmark docstrings):

- curvature: the linear-drive ``k = 0`` point is the gapless Dirac point, whose
  finite-plaquette Berry flux is the sign-ambiguous ``+/- pi/dk**2`` artifact.
  (The linear ``k != 0`` values are numerical zero and are pinned only as
  "still ~0" via ``atol``, not to their roundoff digits.)
- velocity: ``k = 0`` is a band extremum (``v = 0``) / gapless point where the
  velocity is ill-posed.
- quasienergy: the ``|m| = n_trunc`` truncation-edge Floquet blocks are
  distorted by the sideband cutoff and shift when ``n_trunc`` changes, so the
  outermost block on each side is trimmed.

Parameters and helpers are imported from the benchmark modules, so the pinned
configuration tracks the benchmarks themselves. The golden values below were
generated from the toolkit at the current code state; if a legitimate change
shifts them beyond ``rtol`` (e.g. a change to the parallel reduction order),
re-baseline by regenerating these dicts.
"""

