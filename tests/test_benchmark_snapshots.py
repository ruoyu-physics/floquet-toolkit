def _manager(bench, drive):
    """Build a FloquetLocalManager exactly as the given benchmark does."""
    model = DiracModel(bench.DIRAC_PARAMS, drive).to_driven_hamiltonian()
    return FloquetLocalManager(model, bench.FLOQUET_PARAMS)


def _drive_by_label(bench, label):
    return next(drive for name, drive in bench.DRIVES if name == label)


