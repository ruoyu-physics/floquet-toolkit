@pytest.mark.parametrize("label", list(CURVATURE_GOLDEN))
def test_curvature_snapshot(label):
    """Pin HFE-numerical and time-averaged Floquet Berry curvature."""
    bench = curvature_bench
    drive = _drive_by_label(bench, label)
    manager = _manager(bench, drive)
    time_grid = bench.FLOQUET_PARAMS.time_grid(drive.period)
    dk = bench.FLOQUET_PARAMS.dk

    actual, expected = [], []
    for kx, ky, exp_num, exp_flo in CURVATURE_GOLDEN[label]:
        num = manager.compute_hfe_berry_curvature(
            kx, ky, band=bench.BAND, dk=dk, order=bench.HFE_ORDER
        )
        flo = float(
            np.mean(
                manager.compute_instantaneous_berry_curvature(
                    time=time_grid, kx=kx, ky=ky, band=bench.BAND, dk=dk
                )
            )
        )
        actual.extend([num, flo])
        expected.extend([exp_num, exp_flo])

    # atol treats the linear-drive (numerical-zero) curvatures as "still ~0"
    # without pinning their roundoff; it stays well below the smallest real
    # (circular) curvature ~1.7e-17.
    np.testing.assert_allclose(actual, expected, rtol=1e-6, atol=1e-19)


@pytest.mark.parametrize("label", list(VELOCITY_GOLDEN))
def test_velocity_snapshot(label):
    """Pin HFE-numerical and time-averaged Floquet group-velocity speeds."""
    bench = velocity_bench
    drive = _drive_by_label(bench, label)
    manager = _manager(bench, drive)
    time_grid = bench.FLOQUET_PARAMS.time_grid(drive.period)
    dk = bench.FLOQUET_PARAMS.dk

    actual, expected = [], []
    for kx, ky, exp_num, exp_flo in VELOCITY_GOLDEN[label]:
        num_vx, num_vy = manager.compute_hfe_velocity(
            kx, ky, band=bench.BAND, order=bench.HFE_ORDER, dk=dk
        )
        flo_vx_t, flo_vy_t = manager.compute_floquet_velocity(
            time=time_grid, kx=kx, ky=ky, band=bench.BAND, dk=dk
        )
        num_speed = float(np.hypot(num_vx, num_vy))
        flo_speed = float(np.hypot(np.mean(flo_vx_t), np.mean(flo_vy_t)))
        actual.extend([num_speed, flo_speed])
        expected.extend([exp_num, exp_flo])

    np.testing.assert_allclose(actual, expected, rtol=1e-6)


@pytest.mark.parametrize("label", list(QUASIENERGY_GOLDEN))
def test_quasienergy_spectrum_snapshot(label):
    """Pin the interior extended-space Floquet quasienergy spectrum."""
    bench = quasienergy_bench
    drive = _drive_by_label(bench, label)
    manager = _manager(bench, drive)
    provider = manager.state_provider
    trim = QUASIENERGY_EDGE_BLOCKS * provider.dimension

    for kx, ky, expected in QUASIENERGY_GOLDEN[label]:
        spectrum = np.sort(
            np.asarray(provider.diagonalize_floquet_hamiltonian(kx, ky)[0], dtype=float)
        )
        interior = spectrum[trim : len(spectrum) - trim]
        # atol (in Joules) admits the genuine linear k=0 zeros (~1e-34) while
        # rtol pins the meaningful quasienergies (down to the ~1.8e-24 gap).
        np.testing.assert_allclose(interior, expected, rtol=1e-6, atol=1e-28)
